#import "UIColor+GeographyTheme.h"



@implementation UIColor (GeographyTheme)

+ (UIColor *)oceanBlueLight
{
    return UIColorFromRGB(0x9ad1f1);
}

+ (UIColor *)oceanBlueDark
{
    return UIColorFromRGB(0x5c7d90);
}

+ (UIColor *)mapBeigeLight
{
    return UIColorFromRGB(0xf7f3e8);
}

+ (UIColor *)mapBeigeDark
{
    return UIColorFromRGB(0x94918b);
}

@end
